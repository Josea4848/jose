n=input('Digite algo: ')
print('Seu tipo é', type(n))
print('É composto por números?', n.isnumeric())
print('É composto por letras?', n.isalpha())
print('É composto por letras e números?', n.isalnum())
print('É composto por letra maiuscúlas?', n.isupper())
print('É composto por espaços?', n.isspace())
print('É composto de letras minuscúlas?', n.islower())









